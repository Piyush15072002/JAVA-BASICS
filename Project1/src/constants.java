public class constants {
    public static void main(String[] args) throws Exception {
        // just like in the javascript, const and let
        // where the let variable value can change but the value of the Constant
        // variable cannot change after it is declared
        // same way, using "FINAL" keyword, we can make our variable a constant

        final float pi = 3.14F;

        // pi = 100; now we cannot change this

        System.out.println(pi);
    }
}
